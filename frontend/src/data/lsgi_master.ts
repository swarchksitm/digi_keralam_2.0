// Helper to get LSGI Names
const DISTRICT_NAME_MAPPING: Record<string, string> = {
    'Kasargod': 'Kasaragod',
    'Kasargode': 'Kasaragod',
    // Add other potential misspellings here if needed
};


export const getLSGINames = (districtName: string, type: string): string[] => {
    // Normalize District Name
    const normalizedDistrict = DISTRICT_NAME_MAPPING[districtName] || districtName;

    // console.log(`getLSGINames called with: District='${districtName}' -> '${normalizedDistrict}', Type='${type}'`);

    if (!LSGI_MASTER_LIST[normalizedDistrict]) {
        console.error(`District '${normalizedDistrict}' (raw: ${districtName}) not found in Master List!`);
        return [];
    }
    return LSGI_MASTER_LIST[normalizedDistrict][type] || [];
};


